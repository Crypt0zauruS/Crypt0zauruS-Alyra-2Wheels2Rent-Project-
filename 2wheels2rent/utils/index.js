import SITE_URL from "../config";

export const checkIfMetamaskConnected = async () => {
  if (window.ethereum) {
    try {
      const accounts = await window.ethereum.request({
        method: "eth_requestAccounts",
      });
      if (accounts.length > 0) {
        checkIfPolygonInMetamask();
      }
    } catch (error) {
      console.log(error);
    }
  }
};

export const checkIfPolygonInMetamask = async () => {
  if (window.ethereum) {
    try {
      const chainId = await window.ethereum.request({
        method: "eth_chainId",
      });
      if (chainId !== "0x13882") {
        switchPolygonInMetamask();
      }
    } catch (error) {
      console.log(error);
    }
  }
};

export const switchPolygonInMetamask = async () => {
  if (window.ethereum) {
    try {
      await window.ethereum.request({
        method: "wallet_switchEthereumChain",
        params: [{ chainId: "0x13882" }],
      });
    } catch (error) {
      console.log(error);
      addPolygonToMetamask();
    }
  }
};

export const addPolygonToMetamask = async () => {
  if (window.ethereum) {
    try {
      await window.ethereum.request({
        method: "eth_chainId",
      });
    } catch (error) {
      console.log(error);
      return;
    }
    try {
      await window.ethereum.request({
        method: "wallet_addEthereumChain",
        params: [
          {
            chainId: "0x13882",
            chainName: "Matic Amoy",
            nativeCurrency: {
              name: "MATIC",
              symbol: "MATIC",
              decimals: 18,
            },
            rpcUrls: ["https://polygon-amoy.drpc.org/"],
            blockExplorerUrls: ["https://amoy.polygonscan.com/"],
          },
        ],
      });
    } catch (error) {
      console.log(error);
    }
  }
};

export function ImageUrl(url) {
  return SITE_URL + url;
}

